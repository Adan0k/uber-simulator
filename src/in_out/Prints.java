    public static void printGrid(Entity[][] grid){
        System.out.println("------------------------------------------------------------------------------");
        for (int i = 0; i < grid.length; i++){
            System.out.print("| ");
            for (int j = 0; j < grid[i].length; j++){
                if (grid[i][j] == null){
                    System.out.print("  ");
                    continue;
                }
                System.out.print (grid[i][j].getSymbol());
            }
            System.out.print(" |");
            System.out.println();
        }
        System.out.println("------------------------------------------------------------------------------");
    }
